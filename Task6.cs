using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TaskThree
{
    class Program
    {
        static void Main(string[] args)
        {
            //this is the string we are assigning to be printed backwards
            string str = "This is awesome";

            //this splits the words into sepearate 
            string[] words = str.Split(' ');

            for (int i = 0; i < words.Length; i++)
            {
                Console.WriteLine(words[i]);
            }
      
            //increments the words backwards
            for (int i = words.Length; i > 0; i--)
            
            {
                //displaying it 
                Console.WriteLine(words[i - 1]);
            }

            Console.ReadLine();

        
        }

    }

}
